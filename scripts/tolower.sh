#!/bin/bash
##############################################################################
#func_tolower()
##############################################################################
#功能说明:将字符串转换成小写字母
#参数说明:
#	$1:字符串
#返回值:
#	小写的字符串
#使用方法:
#	str=`func_tolower "string"`
##############################################################################
function func_tolower()
{
	if [ -z "$1" ]; then
		echo "(null)"
	else
		echo "$@" | tr A-Z a-z
	fi
}
